"""Абстрактный базовый класс для провайдеров облачных хранилищ.

Этот файл задаёт «шаблон» (интерфейс), которому должны следовать
все провайдеры: Яндекс.Диск, Mail.ru и другие.
Каждый провайдер обязан реализовать метод get_direct_link().
"""

from abc import ABC, abstractmethod


class CloudProvider(ABC):
    """
    Абстрактный базовый класс для облачного провайдера.

    ABC — это специальный класс из стандартной библиотеки Python.
    Он позволяет создавать «абстрактные» классы — такие, от которых
    нельзя создать объект напрямую. Нужно сначала создать класс-наследник
    и реализовать в нём все абстрактные методы.

    Это как чертёж: сам чертёж — не дом, но по нему можно построить дом.
    """

    @abstractmethod
    def get_direct_link(self, public_url: str) -> str:
        """
        Получает прямую ссылку на скачивание файла по публичной ссылке.

        Аргументы:
            public_url: Публичная ссылка на файл в облачном хранилище.
                        Например: https://disk.yandex.ru/i/abc123

        Возвращает:
            Строку с прямой ссылкой, по которой можно скачать файл.

        Выбрасывает:
            ValueError: Если ссылка некорректна или файл не найден.
            ConnectionError: Если не удалось связаться с сервером.
        """
        pass

    @abstractmethod
    def get_provider_name(self) -> str:
        """
        Возвращает человекочитаемое название провайдера.

        Например: "Яндекс.Диск", "Mail.ru Облако"
        """
        pass
